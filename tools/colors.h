#define BG_LGREEN    "\033[102m"
#define BG_LYELLOW   "\033[103m"
#define BG_LBLUE     "\033[104m"
#define BG_LMAGENTA  "\033[105m"
#define BG_LCYAN     "\033[106m"
#define BG_WHITE     "\033[107m"

#endif // COLORS_H
