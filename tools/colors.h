#define INV          "\033[7m"
#define HID          "\033[8m"

#define UN_BOLD      "\033[21m"
#define UN_DIM       "\033[22m"
#define UN_UL        "\033[24m"
#define UN_INV       "\033[27m"
#define UN_HID       "\033[28m"


// Text color
#define DCOLOR       "\033[39m"
#define BLACK        "\033[30m"
#define RED          "\033[31m"
#define GREEN        "\033[32m"
#define YELLOW       "\033[33m"
#define BLUE         "\033[34m"
#define MAGENTA      "\033[35m"
#define CYAN         "\033[36m"
#define LGRAY        "\033[37m"
#define DGRAY        "\033[90m"
#define LRED         "\033[91m"
#define LGREEN       "\033[92m"
#define LYELLOW      "\033[93m"
#define LBLUE        "\033[94m"
#define LMAGENTA     "\033[95m"
#define LCYAN        "\033[96m"
#define WHITE        "\033[97m"

// Background color
#define BG_DCOLOR    "\033[49m"
#define BG_BLACK     "\033[40m"
